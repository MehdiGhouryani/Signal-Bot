"""
ابزارهای عمومی و کوچیک، مشترک بین چند ماژول.

⚠️ چرا esc() لازمه: همه‌جای ربات از parse_mode=HTML استفاده می‌شه. اگه متن آزاد
کاربر (اسم کوین، توضیحات سیگنال، نام کامل، دلیل پاداش و...) مستقیم و بدون
escape داخل تگ‌های HTML بذاریم، وجود کاراکترهای <, >, & (که کاملاً معمولیه —
مثلاً «قیمت < ۵۰۰۰۰» یا «A&B») باعث می‌شه تلگرام کل پیام رو با خطای
«can't parse entities» رد کنه. با تست شبیه‌سازی‌شده (نه فقط تئوری) این ریسک
تأیید شد. این تابع رو باید دور *هر* متن آزادِ کاربر که داخل پیام HTML میره بذاریم.
"""
import html


def esc(text) -> str:
    """Escape امن برای HTML؛ None/غیر-رشته رو هم بی‌خطر مدیریت می‌کنه."""
    if text is None:
        return ""
    return html.escape(str(text), quote=False)
